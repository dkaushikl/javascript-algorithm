// priority queue
// https://github.com/datastructures-js/priority-queue
class PQ {
  constructor() {
    this.arr = [];
  }

  enqueue = val => {
    this.arr.push(val);
    this.arr.sort((a, b) => b - a);
  };

  dequeue = () => {
    return this.arr.shift();
  };

  peek = () => {
    return this.arr[0];
  };

  reer = () => {
    return this.arr[this.arr.length - 1];
  };
}

var minimumDeviation = nums => {
  const mpq = new PQ();

  nums.forEach(x => {
    if (x % 2 !== 0) {
      mpq.enqueue(x * 2);
    } else {
      mpq.enqueue(x);
    }
  });

  let deviation = mpq.peek() - mpq.reer();
  while (mpq.peek() % 2 === 0) {
    const max = mpq.dequeue();

    mpq.enqueue(max / 2);

    deviation = Math.min(deviation, mpq.peek() - mpq.reer());
  }
  return deviation;
};

