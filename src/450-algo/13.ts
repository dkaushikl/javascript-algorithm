const rearrange = arr => {
  console.time('Function #1');

  let maxResult = Number.MIN_SAFE_INTEGER;

  for (let i = 0; i < arr.length; i++) {
    let sum = arr[i];
    maxResult = Math.max(sum, maxResult);

    if (i !== arr.length - 1) {
      for (let j = i + 1; j < arr.length; j++) {
        sum = arr[j] + sum;
        maxResult = Math.max(sum, maxResult);
      }
    }
  }

  console.timeEnd('Function #1');
  return maxResult;
};

